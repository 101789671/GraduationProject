package cn.gpms.form;

public class TuotrApply {
	private String apId;
	private String tutName;
	private String position;
	private String instituteName;
	private String apType;
	private String apContent;
	private String apState;
	private String apTime;
	private String handleTime;
	
	public String getTutName() {
		return tutName;
	}
	public void setTutName(String tutName) {
		this.tutName = tutName;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getInstituteName() {
		return instituteName;
	}
	public void setInstituteName(String instituteName) {
		this.instituteName = instituteName;
	}
	public String getApState() {
		return apState;
	}
	public void setApState(String apState) {
		this.apState = apState;
	}
	public String getApTime() {
		return apTime;
	}
	public void setApTime(String apTime) {
		this.apTime = apTime;
	}
	public String getApId() {
		return apId;
	}
	public void setApId(String apId) {
		this.apId = apId;
	}
	public String getHandleTime() {
		return handleTime;
	}
	public void setHandleTime(String handleTime) {
		this.handleTime = handleTime;
	}
	public String getApType() {
		return apType;
	}
	public void setApType(String apType) {
		this.apType = apType;
	}
	public String getApContent() {
		return apContent;
	}
	public void setApContent(String apContent) {
		this.apContent = apContent;
	}

}
